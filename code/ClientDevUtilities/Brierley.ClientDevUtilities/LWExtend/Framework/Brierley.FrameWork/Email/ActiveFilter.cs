using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Brierley.FrameWork.Email
{
	internal class ActiveFilter
	{
		public string ListField { get; set; }
		public string CurrentValue { get; set; }

		public ActiveFilter()
		{
		}

		public ActiveFilter(string listField)
			: this(listField, null)
		{
		}

		public ActiveFilter(string listField, string currentValue)
		{
			ListField = listField;
			CurrentValue = currentValue;
		}
	}
}
